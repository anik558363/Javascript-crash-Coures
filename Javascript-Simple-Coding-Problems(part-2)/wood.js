function totalCost(shit, pant, shoe){
    const shirtPrice = 500;
    const pantPrice = 300;
    const shoePrice = 900;

    const total = (shit * shirtPrice) + (pant * pantPrice) + (shoe * shoePrice);
    return total;
}


